# Operations with other types
# Variables come in different types in Python.
# You can see the type of a variable by using type().

# Instructions:
# 1. Add savings and new_savings and assign it to total_savings.
# 2. Use type() to print the resulting type of total_savings.

# Given variables
savings = 100
new_savings = 40

# Add them
total_savings = savings + new_savings

# Print the type
print(type(total_savings))
